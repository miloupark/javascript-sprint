// Data Type
// - 변수에 넣을 수 있는 데이터 유형

// number
let a = 10;
console.log(a); // 10
console.log(typeof a); // number

// string
let b = 'hi';
console.log(b); // hi
console.log(typeof b); // string

// boolean
let c = true;
console.log(c); // true
console.log(typeof c); // boolean

// array
let d = [];
console.log(typeof d); // object

// object
let e = {};
console.log(typeof e); // object
